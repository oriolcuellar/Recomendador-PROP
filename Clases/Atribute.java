public class Atribute {
    //atributes
    private String name;
    private String type;
    //getters
    public String getName() {
        return name;
    }
    public String getType() {
        return type;
    }
    //constructores
    public Atribute(String name, String type) {
        this.name = name;
        this.type = type;
    }
}
